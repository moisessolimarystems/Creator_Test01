template <class T>
class Primitive
{
public:
   bool m_bNumsInDec; 

   Primitive() : m_bNumsInDec(false) {}
   Primitive(bool bNumsInDec) : m_bNumsInDec(bNumsInDec) {}

   /*
    * Template function used to stream a primitive type (eg int, string, etc.) to a buffer. 
    * Currently, it is presumed that all numeric types (signed or otherwise) will use the 
    * default implementation. CSpdCString types are over ridden in a partial implementation.
    */
   size_t Serialize(const T &tVal, wchar_t *pwcsBuf, size_t stBufLen, bool bTransparentOn = false)
   {
      QUADWORD qw((QUADWORD)tVal); 
      return SSerialize(qw, pwcsBuf, stBufLen, m_bNumsInDec); 
   }

   /*
    * Template function used to determine max length of the primitive type(eg int, string, etc.) if it were streamed to a buffer. 
    * Currently, it is presumed that all numeric types (signed or otherwise) will use the 
    * default implementation. CSpdCString types are over ridden in a partial implementation.
    */
   size_t MaxSerializeLen(const T &tVal)
   {
      return SMaxSerializeLen((QUADWORD)tVal, m_bNumsInDec);                        // CR.21547; modified.
   }

   /*
    * Template function used to 
    */
   void Deserialize(const CSpdCString &wsStreamed, T &tVal, bool bTransparentOn = false)
   {
      QUADWORD qw; 
      SDeserialize(wsStreamed, qw, m_bNumsInDec); 
      tVal = (T)qw; 
   }
   //
   // static methods.
   //
   // T = QUADWORD
   //
   static size_t SSerialize(const QUADWORD &tVal, wchar_t *pwcsBuf, size_t stBufLen, bool bNumsInDec)
   {
      int nWriteLen(0); 
      nWriteLen = _snwprintf_s(pwcsBuf, stBufLen, _TRUNCATE,
         bNumsInDec ? L"%I64u" : L"%I64X",
         tVal); 
      if (nWriteLen < 0) ThrowHr(E_FAIL, __WFUNCTION__, L"Failed to convert QUADWORD to string. Buffer too small?"); 

      return (size_t)nWriteLen; 
   }
   static size_t SMaxSerializeLen(QUADWORD qw, bool bNumsInDec)
   {
      return 64; 
   }
   static void SDeserialize(const CSpdCString &wsStreamed, QUADWORD &tVal, bool bNumsInDec)
   {
      tVal = (QUADWORD)_wcstoui64(
         wsStreamed.c_str(), 
         NULL,
         bNumsInDec ? 10 : 16); 
   }
   //
   // CR.21547; added support for T = double
   //
   static size_t SSerialize(const double &tVal, wchar_t *pwcsBuf, size_t stBufLen)
   {
      int nWriteLen(0); 
      nWriteLen = _snwprintf_s(pwcsBuf, stBufLen, _TRUNCATE,
         L"%.3f",
         tVal); 
      if (nWriteLen < 0) ThrowHr(E_FAIL, __WFUNCTION__, L"Failed to convert QUADWORD to string. Buffer too small?"); 

      return (size_t)nWriteLen; 
   }
   static size_t SMaxSerializeLen(double db)
   {
      return 64; 
   }
   static void SDeserialize(const CSpdCString &wsStreamed, double &tVal)
   {
      tVal = wcstod(wsStreamed.c_str(), NULL); 
   }
   //
   // T = CSpdCString
   //
   static size_t SSerialize(const CSpdCString &tVal, wchar_t *pwcsBuf, size_t stBufLen, bool bTransparentOn = false)
   {
      // For performance of very long strings; do an initial check to see if buflen is too small.
      if (tVal.wcslen() > stBufLen) ThrowHr(E_FAIL, __WFUNCTION__, L"Failed to serialize CSpdCString. Buffer too small?\n   BufLen <0x%08zX>  wcslen <0x%08zX> IsTransparencyOn <%u>\n", stBufLen, tVal.wcslen(), bTransparentOn); 

      int nWriteLen(0); 
      nWriteLen = _snwprintf_s(pwcsBuf, stBufLen, _TRUNCATE, 
         L"%s",
         bTransparentOn ? tVal.c_str() : SpdUtils::XmlEscapeString(tVal).c_str()); 
      if (nWriteLen < 0) ThrowHr(E_FAIL, __WFUNCTION__, L"Failed to serialize CSpdCString. Buffer too small?\n   BufLen <0x%08zX>  wcslen <0x%08zX> IsTransparencyOn <%u>\n", stBufLen, tVal.wcslen(), bTransparentOn); 

      return (size_t)nWriteLen; 
   }
   static size_t SMaxSerializeLen(const CSpdCString &ws, bool bNumsInDec = false)
   {
      return ws.size() + 1; 
   }
   /* SDeserialize()
    * SLB 02.may.2016 CR.FIX.21516; Fix for not unescaping string when transparent mode is off (and visa-versa).
   -----------------------------------------------------------------------------------------------*/
   static void SDeserialize(const CSpdCString &wsStreamed, CSpdCString &tVal, bool bTransparentOn = false)
   {
      if (!bTransparentOn) SpdUtils::XmlUnescapeString(wsStreamed, tVal);           // CR.21516; modified.
      else tVal = wsStreamed; 
   }
   //
   // T = CResourceStringIF::TResourceType
   //
   static void SDeserialize(const CSpdCString &wsStreamed, CResourceStringIF::TResourceType &tVal, bool bNumsInDec = false)
   {
      QUADWORD qw; 
      SDeserialize(wsStreamed, qw, bNumsInDec); 
      tVal = (CResourceStringIF::TResourceType)qw; 
   }
   //
   // T = FILETIME
   //
   static size_t SSerialize(const ::FILETIME &tVal, wchar_t *pwcsBuf, size_t stBufLen, bool bNumsInDec)
   {
      //if (m_bIsStreamInLocalTime)
      //{
      //   FileTimeToLocalFileTime(&ftVal, &ftSystem); 
      //}
      SYSTEMTIME stVal; 
      FileTimeToSystemTime(&tVal, &stVal); 
      int nWriteLen(0); 
      nWriteLen = _snwprintf_s(pwcsBuf, stBufLen, _TRUNCATE, 
         L"%04d-%02d-%02d %02d:%02d:%02d.%04d",
         stVal.wYear,
         stVal.wMonth,
         stVal.wDay,
         stVal.wHour,
         stVal.wMinute,
         stVal.wSecond,
         stVal.wMilliseconds % 1000);                                               // CR.5729
      if (nWriteLen < 0) ThrowHr(E_FAIL, __WFUNCTION__, L"Failed to convert FILETIME to string. Buffer too small?"); 

      return (size_t)nWriteLen; 
   }
   static size_t SMaxSerializeLen(::FILETIME tVal, bool bNumsInDec)
   {
      return 25; 
   }
   static void SDeserialize(const CSpdCString &wsStreamed, ::FILETIME &tVal, bool bNumsInDec)
   {
      // Make a working copy of the const input buffer.
      wchar_t wcsCpy[25]; 
      wcscpy_s(wcsCpy, wsStreamed.c_str()); 

      // Set intermediate delimiters to NULL.
      wcsCpy[4] = wcsCpy[7] = wcsCpy[10] = wcsCpy[13] = wcsCpy[16] = wcsCpy[19] = wcsCpy[24] = 0; 

      // Now convert known fixed offsets to their appropriate values.
      wchar_t *pwcNull(NULL); 
      SYSTEMTIME stVal; 
      stVal.wYear =     (WORD)_wcstoi64(wcsCpy+0 , &pwcNull, 10); 
      stVal.wMonth =    (WORD)_wcstoi64(wcsCpy+5 , &pwcNull, 10); 
      stVal.wDay =      (WORD)_wcstoi64(wcsCpy+8 , &pwcNull, 10); 
      stVal.wHour =     (WORD)_wcstoi64(wcsCpy+11, &pwcNull, 10); 
      stVal.wMinute =   (WORD)_wcstoi64(wcsCpy+14, &pwcNull, 10); 
      stVal.wSecond =   (WORD)_wcstoi64(wcsCpy+17, &pwcNull, 10); 
      stVal.wMilliseconds = (WORD)_wcstoi64(wcsCpy+20, &pwcNull, 10) % 1000; 
      stVal.wDayOfWeek = 0; 

      if (SystemTimeToFileTime(&stVal, &tVal) == FALSE) ThrowHr(E_FAIL, __WFUNCTION__, L"Failed to deserialize FILETIME type."); 
   }

}; 


