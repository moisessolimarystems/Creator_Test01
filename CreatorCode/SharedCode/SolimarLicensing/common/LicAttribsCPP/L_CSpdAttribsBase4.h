   //
   virtual size_t Serialize(wchar_t *pwcsBuf, size_t stBufCharLen, bool bUseTag=true, bool bUseContainerTag=false, bool bTransparentOn=false) const 
   {
      size_t stWriteOff(0); 
      stWriteOff += SerializeBegin(pwcsBuf+stWriteOff, stBufCharLen-stWriteOff, bUseTag, bUseContainerTag); 

      Primitive<T> p; 
      stWriteOff += p.Serialize(*this, pwcsBuf+stWriteOff, stBufCharLen-stWriteOff); 

      stWriteOff += SerializeEnd(pwcsBuf+stWriteOff, stBufCharLen-stWriteOff, bUseTag, bUseContainerTag); 
      return stWriteOff; 
   }

