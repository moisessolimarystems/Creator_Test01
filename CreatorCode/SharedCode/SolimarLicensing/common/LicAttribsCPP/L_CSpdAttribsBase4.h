template <>
inline size_t Primitive<CAliasableResourceStringBase>::Serialize(const CAliasableResourceStringBase &tVal, wchar_t *pwcsBuf, size_t stBufLen, bool bTransparentOn)
{
   // Streamed out version of this object looks like:
   //
   //   <t>1</t>
   //   <n>AB0934779523</n>
   size_t stWriteOff(0); 
   int nWriteLen(0); 
   nWriteLen = _snwprintf_s(pwcsBuf+stWriteOff, stBufLen-stWriteOff, _TRUNCATE,
      L"<t>"); 
   if (nWriteLen<0) ThrowHr(E_FAIL, __WFUNCTION__, L"Failed to serialize Aliasable Resource begin tag. Buffer too small?"); 
   stWriteOff += nWriteLen; 

   stWriteOff += SSerialize((QUADWORD)tVal.m_rtType, pwcsBuf+stWriteOff, stBufLen-stWriteOff, m_bNumsInDec);      // CR.21547; modified.

   nWriteLen = _snwprintf_s(pwcsBuf+stWriteOff, stBufLen-stWriteOff, _TRUNCATE,
      L"</t><n>"); 
   if (nWriteLen<0) ThrowHr(E_FAIL, __WFUNCTION__, L"Failed to serialize Aliasable Resource middle tags. Buffer too small?"); 
   stWriteOff += nWriteLen; 

   stWriteOff += SSerialize(tVal.m_wsFullResourceName, pwcsBuf+stWriteOff, stBufLen-stWriteOff); 

   nWriteLen = _snwprintf_s(pwcsBuf+stWriteOff, stBufLen-stWriteOff, _TRUNCATE,
      L"</n>"); 
   if (nWriteLen<0) ThrowHr(E_FAIL, __WFUNCTION__, L"Failed to serialize Aliasable Resource end tags. Buffer too small?"); 
   stWriteOff += nWriteLen; 

   return stWriteOff; 
}

