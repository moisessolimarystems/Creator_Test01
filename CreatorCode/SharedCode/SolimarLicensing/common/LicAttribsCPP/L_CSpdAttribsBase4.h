   virtual void GetResourceMemberList(TAttribResourceList &arlAttribList) const {}; 
   virtual bool IsResourceNeeded(const CAttributeResourceIF *pAttrib) const { return false; }
   virtual void Clear() { T::operator = (m_wsDefaultInit); ClearModified(); }			// CR.32290; modified. CR.21493; modified.
