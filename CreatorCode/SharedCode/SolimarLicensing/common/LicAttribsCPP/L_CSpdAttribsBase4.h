         nWriteLen = _snwprintf_s(
            pwcsBuf+stWriteOff, stBufCharLen-stWriteOff, _TRUNCATE,
            L"</K><V>"); 
         if (nWriteLen<0) ThrowHr(E_FAIL, __WFUNCTION__, L"Insufficient space to serialize map node."); 
