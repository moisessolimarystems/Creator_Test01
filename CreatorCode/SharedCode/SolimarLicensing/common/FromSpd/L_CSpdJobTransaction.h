/*
 * template class: TPropertyResult
 *
 * A simple tuple class which contains a value of class T and a bool to indicate if it has been 
 * modified. Returned by all CSpdJobTransaction::GetXXX() methods.
 *
 * Base class for autogenerated code representing an SPDE transaction object.
 *
 * SLB 22.oct.2015 CR.18552; created.
 * SLB 28.mar.2016 CR.18552.V04; Fix for Access violations when using the casting operator.
 * SLB 24.jan.2017 CR.22914; IsModified() fixed to be const method.
 * SLB 18.jan.2023 CR.32082; changed default constructor initialization to eliminate
 * IntelliSense warning msg 26495.
 */
template <class T>
class TPropertyResult
{
public:
   T m_tVal; 
   bool m_bIsModified; 
public:

   // constructors.
   TPropertyResult() : m_tVal(T()), m_bIsModified(false) {}                         // CR.32082; modified.
   TPropertyResult(const T& tDefault, bool bIsModified) : m_tVal(tDefault), m_bIsModified(bIsModified) {}
   TPropertyResult(const TPropertyResult &cpy) : m_tVal(cpy.m_tVal), m_bIsModified(cpy.m_bIsModified) {}
   TPropertyResult(const T &cpy) : m_tVal(cpy), m_bIsModified(true) {}
   //
   // assignment operators
   //
   TPropertyResult operator = (const TPropertyResult &cpy) { m_tVal = cpy.m_tVal; m_bIsModified = cpy.m_bIsModified; return *this; } 
   T operator = (const T &cpy) { m_tVal = cpy; m_bIsModified = true; return m_tVal; } 
   //
   // casting operator
   //
   operator const T&() const { return m_tVal; }                                     // CR.18552.V04; modified.
   //
   // member access operators.
   //
   T * operator ->() { return &m_tVal; } 
   const T * operator ->() const { return &m_tVal; } 
   //
   // member access methods.
   //
   T & Val() { return m_tVal; } 
   bool IsModified() const { return m_bIsModified; }                                // CR.22914; modified.
   //
   // Other.
   //
   void Clear() { m_tVal.Clear(); m_bIsModified = false; }                          // CR.22914; added.
}; 


